// template literals
let boy1 = "Pramod"
let boy2 = "Nikhil"
// Nikhil is a friend of Pramod
let sentence = `${boy2} is a friend of ${boy1}` // this is a backtick not double quotes
console.log(sentence)

// Escape sequence characters in JS
let fruit = 'Bana\'na' // \' is an escape character
console.log(fruit)
